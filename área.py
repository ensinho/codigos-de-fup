a = float(input('Qual o primeiro valor? '))
b = float(input('Qual o segundo valor? '))
c = float(input('Qual o terceiro valor? '))
pi = 3.14159
areatriangulo = a*c/2
areacirculo = pi*(c*c)
areatrapezio = (a+b)*c/2
areaquadrado = b*b
arearetangulo = a*b
print('Área do triângulo é de: ', areatriangulo)
print('Área o círculo é de: ', areacirculo)
print('Área do trapézio é de: ', areatrapezio)
print('Área do quadrado é de: ', areaquadrado)
print('Área do retângulo é de: ', arearetangulo)
